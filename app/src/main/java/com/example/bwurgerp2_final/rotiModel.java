package com.example.bwurgerp2_final;

public class rotiModel {
    public String nama;
    public int gambar;

    public rotiModel(String nama, int gambar) {
        this.nama = nama;
        this.gambar = gambar;
    }

    public int getGambar() {
        return gambar;
    }

    public String getNama() {
        return nama;
    }


}
